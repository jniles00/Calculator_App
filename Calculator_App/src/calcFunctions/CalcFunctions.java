package calcFunctions;

public class CalcFunctions {

	public float result;

	public float Addition(float numOne, float numTwo)
	{
		result = numOne + numTwo;
		return result;
	}

	public float Subtraction(float numOne, float numTwo)
	{
		result = numOne - numTwo;
		return result;
	}
	
	public float Multiplication(float numOne, float numTwo)
	{
		result = numOne * numTwo;
		return result;
	}
	
	public float Division(float numOne, float numTwo)
	{
		result = numOne / numTwo;
		return result;
	}
}
